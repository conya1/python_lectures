# This program expresses all the mathematical operations 
# Get two numbers from a user
num1 = int(input("Enter a random number :")) # inputs a random number
num2 = int(input("Enter another random number :")) # inputs another random number

add = (num1 + num2) # add the two numbers
divide = (num1 / num2) # divide the two numbers
multiply = (num1 * num2) # multiply the two numbers
subtract = (num1 - num2) # subtract the two numbers

print("The sum of ",num1, " and ",num2, "is :",add) # print the sum of the two numbers
print("The division of ", num1, " and ", num2, "is :", divide) # print the division of the two numbers
print("The multiplication of ", num1, " and ", num2, "is :", multiply) # print the multiplication of the two numbers
print("The subtraction of ", num1, " and ", num2, "is :", subtract) #print the subtraction of the two numbers

print(add,":", divide,":", multiply,":", subtract,) 